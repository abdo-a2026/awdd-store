/**
 * Get all products
 * @returns {Array} All products
 */
function getAllProducts() {
    return products;
}

/**
 * Get product by ID
 * @param {number} id - Product ID
 * @returns {Object|null} Product object or null
 */
function getProductById(id) {
    return products.find(product => product.id === id) || null;
}

/**
 * Get products by type
 * @param {string} type - Product type
 * @returns {Array} Filtered products
 */
function getProductsByType(type) {
    return products.filter(product => product.type === type);
}

/**
 * Search products by name or description
 * @param {string} query - Search query
 * @returns {Array} Matching products
 */
function searchProducts(query) {
    const searchTerm = query.toLowerCase();
    return products.filter(product => 
        product.name.toLowerCase().includes(searchTerm) ||
        product.description.toLowerCase().includes(searchTerm) ||
        product.type.toLowerCase().includes(searchTerm)
    );
}

/**
 * Get products by technology
 * @param {string} tech - Technology name
 * @returns {Array} Products using that technology
 */
function getProductsByTechnology(tech) {
    return products.filter(product => 
        product.technologies.some(technology => 
            technology.toLowerCase() === tech.toLowerCase()
        )
    );
}

/**
 * Get all unique product types
 * @returns {Array} Array of unique types
 */
function getAllTypes() {
    return [...new Set(products.map(product => product.type))];
}

/**
 * Get all unique technologies
 * @returns {Array} Array of unique technologies
 */
function getAllTechnologies() {
    const allTechs = products.flatMap(product => product.technologies);
    return [...new Set(allTechs)];
}

/**
 * Get featured products (first 6)
 * @returns {Array} Featured products
 */
function getFeaturedProducts() {
    return products.slice(0, 6);
}

/**
 * Get random products
 * @param {number} count - Number of products to return
 * @returns {Array} Random products
 */
function getRandomProducts(count = 3) {
    const shuffled = [...products].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

